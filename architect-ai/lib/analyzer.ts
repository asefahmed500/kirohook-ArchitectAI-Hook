export function saveDocs(
  basePath: string,
  docs: {
    architecture: string;
    apiDocs: string;
    changelog: string;
    testing: string;
    diagram: string;
  }
) {
  const docsDir = path.join(basePath, 'docs');
  if (!fs.existsSync(docsDir)) fs.mkdirSync(docsDir, { recursive: true });

  fs.writeFileSync(path.join(docsDir, 'architecture.md'), docs.architecture, 'utf-8');
  fs.writeFileSync(path.join(docsDir, 'api.md'), docs.apiDocs, 'utf-8');
  fs.writeFileSync(path.join(docsDir, 'changelog.md'), docs.changelog, 'utf-8');
  fs.writeFileSync(path.join(docsDir, 'testing.md'), docs.testing, 'utf-8');
  fs.writeFileSync(path.join(docsDir, 'system-diagram.mmd'), docs.diagram, 'utf-8');
}

export function loadDocs(basePath: string) {
  const docsDir = path.join(basePath, 'docs');
  const read = (file: string) => {
    try {
      return fs.readFileSync(path.join(docsDir, file), 'utf-8');
    } catch {
      return '';
    }
  };
  return {
    architecture: read('architecture.md'),
    apiDocs: read('api.md'),
    changelog: read('changelog.md'),
    testing: read('testing.md'),
    diagram: read('system-diagram.mmd'),
  };
}
